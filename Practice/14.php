<p>
    14) Работа светофора для пешеходов запрограммирована следующим образом: в начале каждого часа в течение
    трех минут горит зеленый сигнал,
    затем в течение одной минуты — желтый, а потом в течение одной минуты — красный, затем опять зеленый горит
    три минуты и т. д.
    Используя аргумент функции - вещественное число t, означающее время в минутах, прошедшее с начала очередного часа.
    Определить, сигнал какого цвета горит для пешеходов в этот момент.
    Результат вывести на экран в следующем виде:
    "зеленый" - если горит зеленый цвет, "желтый" - если горит желтый цвет, "красный" - если горит красный цвет.
    Пример для числа 2.5:
    зеленый
    Пример для числа 3:
    желтый
    Пример для числа 4:
    красный
    Пример для числа 5:
    зеленый
</p>
<?php

function test($a)
{
    if((($a % 5) > 0) && (($a % 1) <= 3)){
        return "Зеленый!";
    }
    if((($a % 5) > 3) && (($a % 5) <= 4)){
        return "Желтый!";
    }
    else{
        return "Красный!";
    }
}
echo test(7);